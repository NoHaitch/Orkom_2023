#include <stdio.h>

/* ========== Awnser ========== */
int kamippoina(int n){
    // ~0 = 11111111...11111111
    // ~0 << 1 = 111111...1110
    // ~0 << 2 = 111111...1100
    // ~(~0 << 1) = 0000...0001
    // ~(~0 << 2) = 0000...0011
    // ~(~0 << n+1) = 000...01 with amount of 1 = n+1

    return (~((~0) << (n+1)));
}
